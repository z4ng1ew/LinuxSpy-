#!/usr/bin/env bash

# === БЛОК 1: ФУНКЦИЯ ПРЕОБРАЗОВАНИЯ ЦИФР В КОДЫ ===
get_color() {
    # ПРИЕМ АРГУМЕНТОВ:
    local color_num=$1      # Первая цифра (например, 2 - красный)
    local is_background=$2  # Вторая цифра (1 - если это фон, 0 - если шрифт)
    
    # case - это переключатель (как "if", только для списка вариантов).
    case $color_num in
        1) 
            # ЛОГИКА ТЕРНАРНОГО ОПЕРАТОРА (Короткий IF):
            # Условие: [[ $is_background -eq 1 ]] (Это фон?)
            # && (Тогда): echo "47" (Код белого фона)
            # || (Иначе): echo "97" (Код белого текста)
            [[ $is_background -eq 1 ]] && echo "47" || echo "97" 
            ;;
        2) [[ $is_background -eq 1 ]] && echo "41" || echo "31" ;;
        # ... остальные цвета аналогично ...
        *) echo "" ;; # Звездочка - если пришла неизвестная цифра (напр. 9), вернуть пустоту.
    esac
}

# === БЛОК 2: ПОЛУЧЕНИЕ НАЗВАНИЯ ЦВЕТА (ДЛЯ ОТЧЕТА) ===
get_color_name() {
    # Просто возвращает слово "red", "green" и т.д. вместо цифры.
    case $1 in
        1) echo "white" ;;
        2) echo "red" ;;
        # ...
        *) echo "unknown" ;;
    esac
}