#!/bin/bash



### üìë –°–û–î–ï–†–ñ–ê–ù–ò–ï

1. **–ë–õ–û–ö: –ù–ê–°–¢–†–û–ô–ö–ò –ò –í–ê–õ–ò–î–ê–¶–ò–Ø** (–¢–∞–π–º–µ—Ä –∏ –§–µ–π—Å–∫–æ–Ω—Ç—Ä–æ–ª—å)
2. **–ë–õ–û–ö: –í–°–ü–û–ú–û–ì–ê–¢–ï–õ–¨–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø** (–ö–∞–ª—å–∫—É–ª—è—Ç–æ—Ä –±–∞–π—Ç–æ–≤)
3. **–ë–õ–û–ö: –ê–ù–ê–õ–ò–ó –ü–ê–ü–û–ö** (–°—á–µ—Ç—á–∏–∫ –∏ –¢–û–ü-5 –≥–∏–≥–∞–Ω—Ç–æ–≤)
4. **–ë–õ–û–ö: –ü–û–î–°–ß–ï–¢ –§–ê–ô–õ–û–í** (–û–±—â–∞—è —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫–∞)
5. **–ë–õ–û–ö: –ö–õ–ê–°–°–ò–§–ò–ö–ê–¶–ò–Ø –§–ê–ô–õ–û–í** (–†–∞–∑–±–æ—Ä –ø–æ —Ç–∏–ø–∞–º)
6. **–ë–õ–û–ö: –¢–û–ü-10 –§–ê–ô–õ–û–í** (–†–∞–∑–º–µ—Ä + –£–º–Ω–æ–µ –æ–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ç–∏–ø–∞)
7. **–ë–õ–û–ö: –¢–û–ü-10 –ò–°–ü–û–õ–ù–Ø–ï–ú–´–• –§–ê–ô–õ–û–í** (–•–µ—à–∏—Ä–æ–≤–∞–Ω–∏–µ MD5)
8. **–ë–õ–û–ö: –§–ò–ù–ê–õ** (–†–∞—Å—á–µ—Ç –≤—Ä–µ–º–µ–Ω–∏ —Ä–∞–±–æ—Ç—ã)







# 1. === –ë–õ–û–ö: –ù–ê–°–¢–†–û–ô–ö–ò –ò –í–ê–õ–ò–î–ê–¶–ò–Ø ===
# –¶–µ–ª—å: –ó–∞–ø—É—Å—Ç–∏—Ç—å —Å–µ–∫—É–Ω–¥–æ–º–µ—Ä –∏ –ø—Ä–æ–≤–µ—Ä–∏—Ç—å, —á—Ç–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–µ –≤–≤–µ–ª –µ—Ä—É–Ω–¥—É –≤–º–µ—Å—Ç–æ –ø—É—Ç–∏ –∫ –ø–∞–ø–∫–µ.




 **–°–ø–µ—Ü–∏—Ñ–∏–∫–∞—Ç–æ—Ä `%s` (Seconds)**
* –≠—Ç–æ ¬´—Å—ã—Ä–æ–µ¬ª —á–∏—Å–ª–æ.
* **–ó–∞—á–µ–º –æ–Ω–æ –Ω—É–∂–Ω–æ?** –ö–æ–º–ø—å—é—Ç–µ—Ä—É –æ—á–µ–Ω—å —Å–ª–æ–∂–Ω–æ —Å—á–∏—Ç–∞—Ç—å —Ä–∞–∑–Ω–∏—Ü—É –º–µ–∂–¥—É –¥–∞—Ç–∞–º–∏ –≤ —á–µ–ª–æ–≤–µ—á–µ—Å–∫–æ–º —Ñ–æ—Ä–º–∞—Ç–µ.
* *–ü–æ–ø—Ä–æ–±—É–π –≤—ã—á–µ—Å—Ç—å:* "30 –ú–∞—Ä—Ç–∞ 14:15" –º–∏–Ω—É—Å "2 –§–µ–≤—Ä–∞–ª—è 09:10". –≠—Ç–æ —Å–ª–æ–∂–Ω–æ (—Ä–∞–∑–Ω—ã–µ –¥–Ω–∏ –≤ –º–µ—Å—è—Ü–∞—Ö, –≤–∏—Å–æ–∫–æ—Å–Ω—ã–µ –≥–æ–¥–∞ –∏ —Ç.–¥.).


* **–° `%s` –≤—Å—ë –ø—Ä–æ—Å—Ç–æ:** –≠—Ç–æ –æ–±—ã—á–Ω–∞—è –º–∞—Ç–µ–º–∞—Ç–∏–∫–∞.
* –ö–æ–Ω–µ—Ü (1736000050) –º–∏–Ω—É—Å –ù–∞—á–∞–ª–æ (1736000000) = **50 —Å–µ–∫—É–Ω–¥**.




# –ù–∞—á–∞–ª–æ –æ—Ç—Å—á–µ—Ç–∞ –≤—Ä–µ–º–µ–Ω–∏
# date +%s.%N ‚Äî –ü–æ–ª—É—á–∞–µ–º —Ç–µ–∫—É—â–µ–µ –≤—Ä–µ–º—è. –≠—Ç–æ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–µ–∫—É–Ω–¥, –∫–æ—Ç–æ—Ä—ã–µ –ø—Ä–æ—à–ª–∏ —Å –º–æ–º–µ–Ω—Ç–∞ ¬´–ù–∞—á–∞–ª–∞ —ç–ø–æ—Ö–∏ Unix¬ª (Unix Epoch).    –¢–æ—á–∫–∞ –æ—Ç—Å—á–µ—Ç–∞: 1 —è–Ω–≤–∞—Ä—è 1970 –≥–æ–¥–∞, 00:00:00 (UTC).
# %s ‚Äî –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–µ–∫—É–Ω–¥ —Å –Ω–∞—á–∞–ª–∞ —ç–ø–æ—Ö–∏ Unix (1970 –≥–æ–¥).
# %N ‚Äî –Ω–∞–Ω–æ—Å–µ–∫—É–Ω–¥—ã (–¥–ª—è –≤—ã—Å–æ–∫–æ–π —Ç–æ—á–Ω–æ—Å—Ç–∏).
START_TIME=$(date +%s.%N)

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –∫–æ–ª–∏—á–µ—Å—Ç–≤–∞ –ø–µ—Ä–µ–¥–∞–Ω–Ω—ã—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ ($#)
# $# ‚Äî –°–ø–µ—Ü–∏–∞–ª—å–Ω–∞—è –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è, —Ö—Ä–∞–Ω–∏—Ç –ö–û–õ–ò–ß–ï–°–¢–í–û –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ —Å–∫—Ä–∏–ø—Ç–∞.
# -ne ‚Äî (Not Equal) –ù–µ —Ä–∞–≤–Ω–æ.
# –ï—Å–ª–∏ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ –Ω–µ —Ä–æ–≤–Ω–æ 1, –º—ã —Ä—É–≥–∞–µ–º—Å—è –∏ –ø–æ–∫–∞–∑—ã–≤–∞–µ–º –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é.
if [ $# -ne 1 ]; then
    echo "Usage: $0 <directory_path>"
    echo "Example: $0 /var/log/"
    exit 1 # –í—ã—Ö–æ–¥ —Å –∫–æ–¥–æ–º –æ—à–∏–±–∫–∏
fi

# –°–æ—Ö—Ä–∞–Ω—è–µ–º –∞—Ä–≥—É–º–µ–Ω—Ç –≤ –ø–æ–Ω—è—Ç–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é.
# $1 ‚Äî –≠—Ç–æ –ø–µ—Ä–≤—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç (–ø—É—Ç—å, –∫–æ—Ç–æ—Ä—ã–π –≤–≤–µ–ª —é–∑–µ—Ä).
TARGET_DIR="$1"

# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Å—É—â–µ—Å—Ç–≤–æ–≤–∞–Ω–∏—è –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏
# -d ‚Äî (Directory) –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ —Ç–∞–∫–æ–π –∫–∞—Ç–∞–ª–æ–≥.
# ! ‚Äî –û—Ç—Ä–∏—Ü–∞–Ω–∏–µ (–ï—Å–ª–∏ –∫–∞—Ç–∞–ª–æ–≥ –ù–ï —Å—É—â–µ—Å—Ç–≤—É–µ—Ç).
if [ ! -d "$TARGET_DIR" ]; then
    echo "Error: Directory '$TARGET_DIR' does not exist"
    exit 1
fi

# –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞–ª–∏—á–∏—è –∑–∞–≤–µ—Ä—à–∞—é—â–µ–≥–æ —Å–ª—ç—à–∞
# =~ ‚Äî –û–ø–µ—Ä–∞—Ç–æ—Ä —Å—Ä–∞–≤–Ω–µ–Ω–∏—è —Å —Ä–µ–≥—É–ª—è—Ä–Ω—ã–º –≤—ã—Ä–∞–∂–µ–Ω–∏–µ–º (Regex).
# /$ ‚Äî –®–∞–±–ª–æ–Ω –ø–æ–∏—Å–∫–∞.
#    / ‚Äî –ò—â–µ–º —Å–∏–º–≤–æ–ª —Å–ª—ç—à–∞.
#    $ ‚Äî –Ø–∫–æ—Ä—å –∫–æ–Ω—Ü–∞ —Å—Ç—Ä–æ–∫–∏. –û–∑–Ω–∞—á–∞–µ—Ç "—Å—Ç—Ä–æ–∫–∞ –¥–æ–ª–∂–Ω–∞ –∑–∞–∫–∞–Ω—á–∏–≤–∞—Ç—å—Å—è –∏–º–µ–Ω–Ω–æ —ç—Ç–∏–º —Å–∏–º–≤–æ–ª–æ–º".
# –ï—Å–ª–∏ –ø—É—Ç—å –ù–ï –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ —Å–ª—ç—à, —Å–∫—Ä–∏–ø—Ç –ø—Ä–æ—Å–∏—Ç –µ–≥–æ –¥–æ–±–∞–≤–∏—Ç—å.
if [[ ! "$TARGET_DIR" =~ /$ ]]; then
    echo "Error: Directory path must end with '/'"
    echo "Example: $0 ${TARGET_DIR}/"
    exit 1
fi

echo "Analyzing directory: $TARGET_DIR"
echo "Please wait..."
echo

# 2. === –ë–õ–û–ö: –í–°–ü–û–ú–û–ì–ê–¢–ï–õ–¨–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø (bytes_to_human) ===
# –¶–µ–ª—å: –ü—Ä–µ–≤—Ä–∞—Ç–∏—Ç—å –¥–ª–∏–Ω–Ω—ã–µ –Ω–µ–ø–æ–Ω—è—Ç–Ω—ã–µ —á–∏—Å–ª–∞ (–±–∞–π—Ç—ã) –≤ –∫—Ä–∞—Å–∏–≤—ã–µ GB, MB, KB.

bytes_to_human() {
    local bytes=$1 # –ü—Ä–∏–Ω–∏–º–∞–µ–º —á–∏—Å–ª–æ –±–∞–π—Ç –≤–æ –≤–Ω—É—Ç—Ä—å —Ñ—É–Ω–∫—Ü–∏–∏
    
    # –ú–∞—Ç–µ–º–∞—Ç–∏–∫–∞ Bash $(( ... )):
    # Bash —É–º–µ–µ—Ç –¥–µ–ª–∏—Ç—å —Ç–æ–ª—å–∫–æ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω–æ (–æ—Ç–±—Ä–∞—Å—ã–≤–∞–µ—Ç –¥—Ä–æ–±–Ω—É—é —á–∞—Å—Ç—å).
    # 1024 * 1024 * 1024 = 1 –ì–∏–≥–∞–±–∞–π—Ç
    local gb=$((bytes / 1024 / 1024 / 1024))
    local mb=$((bytes / 1024 / 1024))
    local kb=$((bytes / 1024))
    
    # –õ–æ–≥–∏–∫–∞ –≤—ã–±–æ—Ä–∞ –º–∞—Å—à—Ç–∞–±–∞:
    # -gt ‚Äî (Greater Than) –ë–æ–ª—å—à–µ —á–µ–º.
    if [ $gb -gt 0 ]; then
        echo "${gb} GB"
    elif [ $mb -gt 0 ]; then
        echo "${mb} MB"
    elif [ $kb -gt 0 ]; then
        echo "${kb} KB"
    else
        echo "${bytes} bytes"
    fi
}

# 3. === –ë–õ–û–ö: –ê–ù–ê–õ–ò–ó –ü–ê–ü–û–ö ===
# –¶–µ–ª—å: –ü–æ—Å—á–∏—Ç–∞—Ç—å, —Å–∫–æ–ª—å–∫–æ –≤—Å–µ–≥–æ –ø–∞–ø–æ–∫ –≤–Ω—É—Ç—Ä–∏, –∏ –Ω–∞–π—Ç–∏ —Å–∞–º—ã–µ —Ç—è–∂–µ–ª—ã–µ –∏–∑ –Ω–∏—Ö.

# 1. –ü–æ–¥—Å—á–µ—Ç –ø–∞–ø–æ–∫
# find "$TARGET_DIR" ‚Äî –ò—â–µ–º –≤ —Ü–µ–ª–µ–≤–æ–π –ø–∞–ø–∫–µ.
# -type d ‚Äî (Directory) –ò—â–µ–º —Ç–æ–ª—å–∫–æ –ø–∞–ø–∫–∏.
# 2>/dev/null ‚Äî –û—à–∏–±–∫–∏ ("Permission denied") –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º –≤ "—á–µ—Ä–Ω—É—é –¥—ã—Ä—É", —á—Ç–æ–±—ã –Ω–µ –∑–∞—Å–æ—Ä—è—Ç—å —ç–∫—Ä–∞–Ω.
# wc -l ‚Äî (Word Count Lines) –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –Ω–∞–π–¥–µ–Ω–Ω—ã—Ö —Å—Ç—Ä–æ–∫.
FOLDER_COUNT=$(find "$TARGET_DIR" -type d 2>/dev/null | wc -l)
echo "Total number of folders (including all nested ones) = $FOLDER_COUNT"



# 2. TOP 5 –ø–∞–ø–æ–∫ –ø–æ —Ä–∞–∑–º–µ—Ä—É
echo "TOP 5 folders of maximum size arranged in descending order (path and size):"

# –°–ª–æ–∂–Ω—ã–π –∫–æ–Ω–≤–µ–π–µ—Ä (Pipeline):
# –≠—Ç–∞–ø 1: –ù–∞—Ö–æ–¥–∏–º –≤—Å–µ –ø–∞–ø–∫–∏ –∏ –∑–∞–ø—É—Å–∫–∞–µ–º —Ü–∏–∫–ª while.
find "$TARGET_DIR" -type d 2>/dev/null | while read -r dir; do
    # –≠—Ç–∞–ø 2: –î–ª—è –∫–∞–∂–¥–æ–π –ø–∞–ø–∫–∏ ($dir) —Å—á–∏—Ç–∞–µ–º –µ—ë —Ä–∞–∑–º–µ—Ä.
    # du -sb ‚Äî (Disk Usage):
    #    -s (Summarize): –ü–æ–∫–∞–∑–∞—Ç—å –æ–±—â–∏–π —Ä–∞–∑–º–µ—Ä –ø–∞–ø–∫–∏, –∞ –Ω–µ –ø–µ—Ä–µ—á–∏—Å–ª—è—Ç—å —Ñ–∞–π–ª—ã –≤–Ω—É—Ç—Ä–∏.
    #    -b (Bytes): –ü–æ–∫–∞–∑–∞—Ç—å —Ä–∞–∑–º–µ—Ä –≤ –±–∞–π—Ç–∞—Ö (–¥–ª—è —Ç–æ—á–Ω–æ—Å—Ç–∏).
    # awk '{print $1}' ‚Äî –ë–µ—Ä–µ–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ –∏–∑ –≤—ã–≤–æ–¥–∞ (—Å–∞–º–æ —á–∏—Å–ª–æ), –æ—Ç–±—Ä–∞—Å—ã–≤–∞—è –∏–º—è –ø–∞–ø–∫–∏.
    size_bytes=$(du -sb "$dir" 2>/dev/null | awk '{print $1}')
    
    # –ü–µ—á–∞—Ç–∞–µ–º "–†–∞–∑–º–µ—Ä|–ü—É—Ç—å". –†–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å | –Ω—É–∂–µ–Ω –¥–ª—è —É–¥–æ–±–Ω–æ–π —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∏ –¥–∞–ª—å—à–µ.
    echo "$size_bytes|$dir"
done | sort -t'|' -k1 -rn | head -5 | while IFS='|' read -r size_bytes path; do
    # –≠—Ç–∞–ø 3: –°–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –∏ —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ.
    # sort -t'|' ‚Äî –ì–æ–≤–æ—Ä–∏–º, —á—Ç–æ –∫–æ–ª–æ–Ω–∫–∏ —Ä–∞–∑–¥–µ–ª–µ–Ω—ã –≤–µ—Ä—Ç–∏–∫–∞–ª—å–Ω–æ–π —á–µ—Ä—Ç–æ–π.
    # -k1 ‚Äî –°–æ—Ä—Ç–∏—Ä—É–µ–º –ø–æ 1-–π –∫–æ–ª–æ–Ω–∫–µ (—Ä–∞–∑–º–µ—Ä—É).
    # -rn ‚Äî (Reverse Numeric): –ß–∏—Å–ª–æ–≤–∞—è —Å–æ—Ä—Ç–∏—Ä–æ–≤–∫–∞ –ø–æ —É–±—ã–≤–∞–Ω–∏—é (–æ—Ç –±–æ–ª—å—à–µ–≥–æ –∫ –º–µ–Ω—å—à–µ–º—É).
    # head -5 ‚Äî –û—Ç—Ä–µ–∑–∞–µ–º —Ç–æ–ª—å–∫–æ –ø–µ—Ä–≤—ã–µ 5 —Å—Ç—Ä–æ–∫.
    
    # IFS='|' ‚Äî (Internal Field Separator). –ì–æ–≤–æ—Ä–∏–º –∫–æ–º–∞–Ω–¥–µ read: "–†–∞–∑–±–∏–≤–∞–π —Å—Ç—Ä–æ–∫—É –Ω–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ —Ç–∞–º, –≥–¥–µ –≤–∏–¥–∏—à—å —Å–∏–º–≤–æ–ª |".
    
    # –ü—Ä–µ–≤—Ä–∞—â–∞–µ–º –±–∞–π—Ç—ã –≤ –∫—Ä–∞—Å–æ—Ç—É —á–µ—Ä–µ–∑ –Ω–∞—à—É —Ñ—É–Ω–∫—Ü–∏—é
    size_human=$(bytes_to_human "$size_bytes")
    
    # –ö–æ—Å–º–µ—Ç–∏—á–µ—Å–∫–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞: –¥–æ–±–∞–≤–ª—è–µ–º —Å–ª—ç—à –≤ –∫–æ–Ω—Ü–µ –ø—É—Ç–∏, –µ—Å–ª–∏ –µ–≥–æ –Ω–µ—Ç (–¥–ª—è –∫—Ä–∞—Å–æ—Ç—ã –æ—Ç—á–µ—Ç–∞).
    if [[ ! "$path" =~ /$ ]]; then
        path="${path}/"
    fi
    # –ü–µ—á–∞—Ç–∞–µ–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç –¥–ª—è awk
    echo "$path|$size_human"
done | awk -F'|' '{print NR " - " $1 ", " $2}'
# awk -F'|' ‚Äî –û–ø—è—Ç—å –∏—Å–ø–æ–ª—å–∑—É–µ–º | –∫–∞–∫ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª—å.
# NR ‚Äî (Number of Record) –í—Å—Ç—Ä–æ–µ–Ω–Ω—ã–π —Å—á–µ—Ç—á–∏–∫ awk (–Ω–æ–º–µ—Ä —Å—Ç—Ä–æ–∫–∏: 1, 2, 3...).
# $1 ‚Äî –ü—É—Ç—å, $2 ‚Äî –†–∞–∑–º–µ—Ä.

# 4. === –ë–õ–û–ö: –ü–û–î–°–ß–ï–¢ –§–ê–ô–õ–û–í ===
# –¶–µ–ª—å: –ü—Ä–æ—Å—Ç–æ –ø–æ—Å—á–∏—Ç–∞—Ç—å –æ–±—â–µ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ñ–∞–π–ª–æ–≤ –≤—Å–µ—Ö —Ç–∏–ø–æ–≤.

TOTAL_FILES=$(find "$TARGET_DIR" -type f 2>/dev/null | wc -l)
# -type f ‚Äî (File) –ò—â–µ–º —Ç–æ–ª—å–∫–æ —Ñ–∞–π–ª—ã (–Ω–µ –ø–∞–ø–∫–∏, –Ω–µ —Å—Å—ã–ª–∫–∏).
echo "Total number of files = $TOTAL_FILES"

# 5. === –ë–õ–û–ö: –ö–õ–ê–°–°–ò–§–ò–ö–ê–¶–ò–Ø –§–ê–ô–õ–û–í ===
# –¶–µ–ª—å: –ü–æ—Å—á–∏—Ç–∞—Ç—å —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –ø–æ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è–º (—Å–∫–æ–ª—å–∫–æ –∫–æ–Ω—Ñ–∏–≥–æ–≤, –ª–æ–≥–æ–≤ –∏ —Ç.–¥.).

echo "Number of:"

# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–æ–Ω–Ω—ã–µ —Ñ–∞–π–ª—ã
# -name "*.conf" ‚Äî –ò—â–µ–º —Ñ–∞–π–ª—ã, –∏–º—è –∫–æ—Ç–æ—Ä—ã—Ö –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è –Ω–∞ .conf.
# –ö–∞–≤—ã—á–∫–∏ "*.conf" –æ–±—è–∑–∞—Ç–µ–ª—å–Ω—ã, —á—Ç–æ–±—ã Bash –Ω–µ –ø—ã—Ç–∞–ª—Å—è —Ä–∞—Å–∫—Ä—ã—Ç—å –∑–≤–µ–∑–¥–æ—á–∫—É —Ä–∞–Ω—å—à–µ –≤—Ä–µ–º–µ–Ω–∏.
CONF_COUNT=$(find "$TARGET_DIR" -type f -name "*.conf" 2>/dev/null | wc -l)
echo "Configuration files (with the .conf extension) = $CONF_COUNT"

# –¢–µ–∫—Å—Ç–æ–≤—ã–µ —Ñ–∞–π–ª—ã
TEXT_COUNT=$(find "$TARGET_DIR" -type f -name "*.txt" 2>/dev/null | wc -l)
echo "Text files = $TEXT_COUNT"

# –ò—Å–ø–æ–ª–Ω—è–µ–º—ã–µ —Ñ–∞–π–ª—ã
# -executable ‚Äî –°–ø–µ—Ü–∏–∞–ª—å–Ω—ã–π —Ñ–ª–∞–≥ find. –ò—â–µ—Ç —Ñ–∞–π–ª—ã, —É –∫–æ—Ç–æ—Ä—ã—Ö —Å—Ç–æ–∏—Ç –±–∏—Ç –∏—Å–ø–æ–ª–Ω–µ–Ω–∏—è (+x) (—Å–∫—Ä–∏–ø—Ç—ã, –ø—Ä–æ–≥—Ä–∞–º–º—ã).
EXEC_COUNT=$(find "$TARGET_DIR" -type f -executable 2>/dev/null | wc -l)
echo "Executable files = $EXEC_COUNT"

# –õ–æ–≥ —Ñ–∞–π–ª—ã
LOG_COUNT=$(find "$TARGET_DIR" -type f -name "*.log" 2>/dev/null | wc -l)
echo "Log files (with the extension .log) = $LOG_COUNT"

# –ê—Ä—Ö–∏–≤—ã
# –ó–¥–µ—Å—å –∏—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å–ª–æ–∂–Ω–∞—è –ª–æ–≥–∏–∫–∞ –ì–†–£–ü–ü–ò–†–û–í–ö–ò –≤ find.
# \( ... \) ‚Äî –≠–∫—Ä–∞–Ω–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ —Å–∫–æ–±–∫–∏ –æ–±—ä–µ–¥–∏–Ω—è—é—Ç —É—Å–ª–æ–≤–∏—è –≤ –≥—Ä—É–ø–ø—É.
# -o ‚Äî (OR / –ò–õ–ò). –õ–æ–≥–∏–∫–∞: "–Ω–∞–π—Ç–∏ —Ñ–∞–π–ª, –µ—Å–ª–∏ –∏–º—è .zip –ò–õ–ò –∏–º—è .tar –ò–õ–ò –∏–º—è .gz ..."
ARCHIVE_COUNT=$(find "$TARGET_DIR" -type f \( -name "*.zip" -o -name "*.tar" -o -name "*.gz" -o -name "*.bz2" -o -name "*.xz" -o -name "*.7z" -o -name "*.rar" -o -name "*.tar.gz" -o -name "*.tgz" \) 2>/dev/null | wc -l)
echo "Archive files = $ARCHIVE_COUNT"

# –°–∏–º–≤–æ–ª–∏—á–µ—Å–∫–∏–µ —Å—Å—ã–ª–∫–∏
# -type l ‚Äî (Link) –ò—â–µ—Ç —è—Ä–ª—ã–∫–∏ (symlinks).
SYMLINK_COUNT=$(find "$TARGET_DIR" -type l 2>/dev/null | wc -l)
echo "Symbolic links = $SYMLINK_COUNT"

# 6. === –ë–õ–û–ö: –¢–û–ü-10 –§–ê–ô–õ–û–í (–£–ú–ù–û–ï –û–ü–†–ï–î–ï–õ–ï–ù–ò–ï –¢–ò–ü–ê) ===
# –¶–µ–ª—å: –ù–∞–π—Ç–∏ —Å–∞–º—ã–µ –±–æ–ª—å—à–∏–µ —Ñ–∞–π–ª—ã –∏ –ø–æ–ø—ã—Ç–∞—Ç—å—Å—è —É–≥–∞–¥–∞—Ç—å, —á—Ç–æ —ç—Ç–æ, –¥–∞–∂–µ –µ—Å–ª–∏ –Ω–µ—Ç —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è.

echo "TOP 10 files of maximum size arranged in descending order (path, size and type):"

# –û–ø—Ç–∏–º–∏–∑–∞—Ü–∏—è —Å–∫–æ—Ä–æ—Å—Ç–∏:
# –í–º–µ—Å—Ç–æ —Ü–∏–∫–ª–∞ —Å 'du', –º—ã –∏—Å–ø–æ–ª—å–∑—É–µ–º -printf –≤ –∫–æ–º–∞–Ω–¥–µ find.
# %s ‚Äî —Ä–∞–∑–º–µ—Ä —Ñ–∞–π–ª–∞ –≤ –±–∞–π—Ç–∞—Ö.
# %p ‚Äî –ø–æ–ª–Ω—ã–π –ø—É—Ç—å –∫ —Ñ–∞–π–ª—É.
# \n ‚Äî –ø–µ—Ä–µ–≤–æ–¥ —Å—Ç—Ä–æ–∫–∏.
# –≠—Ç–æ —Ä–∞–±–æ—Ç–∞–µ—Ç –≤ 100 —Ä–∞–∑ –±—ã—Å—Ç—Ä–µ–µ, —á–µ–º –≤—ã–∑–æ–≤ du –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ñ–∞–π–ª–∞.
find "$TARGET_DIR" -type f 2>/dev/null -printf "%s|%p\n" | sort -t'|' -k1 -rn | head -10 | while IFS='|' read -r size_bytes path; do
    size_human=$(bytes_to_human "$size_bytes")
    
    # –û–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ —Ç–∏–ø–∞ —Ñ–∞–π–ª–∞
    type="unknown"
    basename_file=$(basename "$path") # –ü–æ–ª—É—á–∞–µ–º —á–∏—Å—Ç–æ–µ –∏–º—è —Ñ–∞–π–ª–∞ (–±–µ–∑ –ø—É—Ç–∏), –Ω–∞–ø—Ä–∏–º–µ—Ä –∏–∑ /var/log/syslog –ø–æ–ª—É—á–∏–º syslog.
    
    # case "$path" in ‚Äî –ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –≤–µ—Å—å –ø—É—Ç—å (–ø—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—é).
    case "$path" in
        *.conf) type="conf" ;;
        *.txt) type="text" ;;
        *.log) type="log" ;;
        *.journal) type="journal" ;;
        *.zip|*.tar|*.gz|*.bz2|*.xz|*.7z|*.rar|*.tar.gz|*.tgz) type="archive" ;; # | —Ä–∞–±–æ—Ç–∞–µ—Ç –∫–∞–∫ –ò–õ–ò
        *.exe) type="exe" ;;
        *.sh|*.bash) type="script" ;;
        *.bin) type="binary" ;;
        *.so|*.so.*) type="library" ;;
        *.deb|*.rpm) type="package" ;;
        *)
            # –ï—Å–ª–∏ —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è –Ω–µ—Ç –∏–ª–∏ –æ–Ω–æ –Ω–µ–∏–∑–≤–µ—Å—Ç–Ω–æ, —Å–º–æ—Ç—Ä–∏–º –Ω–∞ –∏–º—è —Ñ–∞–π–ª–∞.
            # –ß–∞—Å—Ç–æ –≤ Linux –ª–æ–≥–∏ –Ω–µ –∏–º–µ—é—Ç —Ä–∞—Å—à–∏—Ä–µ–Ω–∏—è .log (–Ω–∞–ø—Ä–∏–º–µ—Ä, syslog, messages).
            case "$basename_file" in
                syslog*|messages*|daemon*|auth*|kern*|debug*|mail*|user*|cron*) type="log" ;;
            esac
            
            # –ï—Å–ª–∏ —Ç–∏–ø –≤—Å–µ –µ—â–µ unknown, –ø—Ä–æ–≤–µ—Ä—è–µ–º –ø—Ä–∞–≤–∞ –¥–æ—Å—Ç—É–ø–∞.
            # -x ‚Äî (Executable) –ü—Ä–æ–≤–µ—Ä–∫–∞: "–Ø–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ñ–∞–π–ª –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º?".
            if [ "$type" = "unknown" ] && [ -x "$path" ]; then
                type="executable"
            fi
            ;;
    esac
    
    echo "$path|$size_human|$type"
done | awk -F'|' '{print NR " - " $1 ", " $2 ", " $3}'

# 7. === –ë–õ–û–ö: –¢–û–ü-10 –ò–°–ü–û–õ–ù–Ø–ï–ú–´–• –§–ê–ô–õ–û–í (MD5) ===
# –¶–µ–ª—å: –ù–∞–π—Ç–∏ —Å–∞–º—ã–µ –∂–∏—Ä–Ω—ã–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã/—Å–∫—Ä–∏–ø—Ç—ã –∏ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∏—Ö —É–Ω–∏–∫–∞–ª—å–Ω—ã–π –æ—Ç–ø–µ—á–∞—Ç–æ–∫ (—Ö–µ—à).

echo "TOP 10 executable files of the maximum size arranged in descending order (path, size and MD5 hash of file):"
find "$TARGET_DIR" -type f -executable 2>/dev/null -printf "%s|%p\n" | sort -t'|' -k1 -rn | head -10 | while IFS='|' read -r size_bytes path; do
    size_human=$(bytes_to_human "$size_bytes")
    
    # –í—ã—á–∏—Å–ª–µ–Ω–∏–µ MD5 —Ö–µ—à–∞ (–ö—Ä–æ—Å—Å–ø–ª–∞—Ç—Ñ–æ—Ä–º–µ–Ω–Ω–æ—Å—Ç—å)
    # command -v ‚Äî –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–∞ –ª–∏ –ø—Ä–æ–≥—Ä–∞–º–º–∞.
    if command -v md5sum &> /dev/null; then
        # Linux —Å—Ç–∏–ª—å (md5sum). awk '{print $1}' –æ—Ç—Ä–µ–∑–∞–µ—Ç –∏–º—è —Ñ–∞–π–ª–∞, –æ—Å—Ç–∞–≤–ª—è—è —Ç–æ–ª—å–∫–æ —Ö–µ—à.
        hash=$(md5sum "$path" 2>/dev/null | awk '{print $1}')
    elif command -v md5 &> /dev/null; then
        # MacOS/BSD —Å—Ç–∏–ª—å (md5). –û–ø—Ü–∏—è -q (quiet) –≤—ã–≤–æ–¥–∏—Ç —Ç–æ–ª—å–∫–æ —Ö–µ—à.
        hash=$(md5 -q "$path" 2>/dev/null)
    else
        hash="unavailable"
    fi
    
    # [ -z "$hash" ] ‚Äî –ï—Å–ª–∏ –ø–µ—Ä–µ–º–µ–Ω–Ω–∞—è –ø—É—Å—Ç–∞—è (—Ö–µ—à–∏—Ä–æ–≤–∞–Ω–∏–µ –Ω–µ —É–¥–∞–ª–æ—Å—å –ø–æ –∫–∞–∫–æ–π-—Ç–æ –ø—Ä–∏—á–∏–Ω–µ).
    [ -z "$hash" ] && hash="unavailable"
    
    echo "$path|$size_human|$hash"
done | awk -F'|' '{print NR " - " $1 ", " $2 ", " $3}'

# 8. === –ë–õ–û–ö: –§–ò–ù–ê–õ (–í–†–ï–ú–Ø –í–´–ü–û–õ–ù–ï–ù–ò–Ø) ===
# –¶–µ–ª—å: –ü–æ—Å—á–∏—Ç–∞—Ç—å, —Å–∫–æ–ª—å–∫–æ —Å–µ–∫—É–Ω–¥ —Å–∫—Ä–∏–ø—Ç —Ä–∞–±–æ—Ç–∞–ª (—Å —Ç–æ—á–Ω–æ—Å—Ç—å—é –¥–æ –¥–æ–ª–µ–π —Å–µ–∫—É–Ω–¥—ã).

# –ö–æ–Ω–µ—Ü –æ—Ç—Å—á–µ—Ç–∞ –≤—Ä–µ–º–µ–Ω–∏
END_TIME=$(date +%s.%N)

# –í—ã—á–∏—Å–ª–µ–Ω–∏–µ —Ä–∞–∑–Ω–∏—Ü—ã
# bc ‚Äî (Basic Calculator). –°—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—ã–π –∫–∞–ª—å–∫—É–ª—è—Ç–æ—Ä Linux, —É–º–µ–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Å –ø–ª–∞–≤–∞—é—â–µ–π —Ç–æ—á–∫–æ–π (–¥—Ä–æ–±–Ω—ã–º–∏ —á–∏—Å–ª–∞–º–∏), –≤ –æ—Ç–ª–∏—á–∏–µ –æ—Ç —Å–∞–º–æ–≥–æ Bash.
# –ú—ã –ø–µ—Ä–µ–¥–∞–µ–º –µ–º—É —Å—Ç—Ä–æ–∫—É "–ö–æ–Ω–µ—Ü - –ù–∞—á–∞–ª–æ" –∏ –ø—Ä–æ—Å–∏–º –ø–æ—Å—á–∏—Ç–∞—Ç—å.
EXECUTION_TIME=$(echo "$END_TIME - $START_TIME" | bc 2>/dev/null)

# –ö–æ—Å–º–µ—Ç–∏—á–µ—Å–∫–∞—è –ø—Ä–∞–≤–∫–∞ –¥–ª—è —á–∏—Å–µ–ª –º–µ–Ω—å—à–µ 1.
# –ï—Å–ª–∏ bc –≤–µ—Ä–Ω—É–ª ".45", –º—ã —Ö–æ—Ç–∏–º –≤–∏–¥–µ—Ç—å "0.45".
# =~ ^\. ‚Äî Regex: "–ù–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å —Ç–æ—á–∫–∏".
if [[ -z "$EXECUTION_TIME" ]] || [[ "$EXECUTION_TIME" =~ ^\. ]]; then
    EXECUTION_TIME="0$EXECUTION_TIME"
fi

echo "Script execution time (in seconds) = $EXECUTION_TIME"





















